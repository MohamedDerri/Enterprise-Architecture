package client;

public class Inputs {

    int num1;
    int num2;

    public Inputs() {
    }

    public Inputs(int num1, int num2) {
        this.num1 = num1;
        this.num2 = num2;
    }

    public int getX() {
        return num1;
    }

    public int getY() {
        return num2;
    }
}
